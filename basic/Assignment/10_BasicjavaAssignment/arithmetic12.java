public class arithmetic12 {
    public static void main(String[] args) {
        //1.6*(12-2)/2+7.8-1.5 
        float a = 1.6f;
        int b = 12;
        int c = 2;
        float d = 7.8f;
        float e = 1.5f;
        float f = a * (b - c) / c + d - e;
        System.out.println("1.6*(12-2)/2+7.8-1.5 is " + f);
    }
}
