public class Ifelse_if {

    public static void main(String[] args) {
        int a = 15;
        if (a == 1) {
            System.out.println("If part");
        } else if (a > 10) {
            System.out.println("if else part");
        } else {
            System.out.println("else part");
        }
    }
    
}
